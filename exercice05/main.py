def sum(a, b):
    """
    Fonction qui retourne la somme de deux nombres

    :param a (int)
    :param b (int)
    :return la somme des deux nombres
    """
    return a + b
 
def subtraction(a, b):
    """
    Fonction qui retourne la difference de deux nombres
    :param a (int)
    :param b (int)
    :return la difference des deux nombres
    """
    return a - b
